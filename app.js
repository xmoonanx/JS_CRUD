const calculator = {
  add: function (a, b) {
    console.log(a + b);
  },
  minus: function (a, b) {
    console.log(a - b);
  },
  div: function (a, b) {
    console.log(a / b);
  },
  multi: function (a, b) {
    console.log(a * b);
  },
  powerof: function (a, b) {
    console.log(a ** b);
  },
};

calculator.add(6, 5);
calculator.minus(7, 4);
calculator.div(9, 3);
calculator.multi(5, 2);
calculator.powerof(2, 4);
